package com.example.jsfdemo.domain;


public class Person {
	
	private String name = "unknown";
	private int yob = 1900;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getYob() {
		return yob;
	}
	public void setYob(int yob) {
		this.yob = yob;
	}

}
